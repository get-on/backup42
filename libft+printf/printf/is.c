#include "libft.h"
#include "ft_printf.h"

int			is_valid(char c)
{
	if ((c == 'U') || (c == 'u') ||
			(c == 'x') || (c == 'X') ||
			(c == 'c') || (c == 'C') ||
			(c == 'd') || (c == 'D') ||
			(c == 'o') || (c == 'O') ||
			(c == 's') || (c == 'S') ||
			(c == 'i') ||
			(c == 'p') ||
			(c == '#') ||
			(c == '0') ||
			(c == '-') ||
			(c == '+') ||
			(c == ' ') ||
			(c == '.') ||
			(c == '*') ||
			(c == 'F') ||
			(c == 'h') ||
			(c == 'l') ||
			(c == 'j') ||
			(c == 'z') ||
			(ft_isdigit(c)))
		return (1);
	return (0);
}

int			is_flag(char c)
{
	if ((c == '#') ||
			(c == '0') ||
			(c == '-') ||
			(c == '+') ||
			(c == 'F') ||
			(c == '*') ||
			(c == ' '))
		return (1);
	return (0);
}

int			is_type(char c)
{
	if ((c == 'i') ||
			(c == 'u') ||
			(c == 'x') ||
			(c == 'X') ||
			(c == 'c') ||
			(c == 'C') ||
			(c == 'd') ||
			(c == 'D') ||
			(c == 'o') ||
			(c == 'O') ||
			(c == 'U') ||
			(c == 's') ||
			(c == 'S') ||
			(c == 'p'))
		return (1);
	return (0);
}

int			is__dot(int c)
{
	if (c == '.')
		return (1);
	return (0);
}

int			is_l_modifier(char c)
{
	if ((c == 'h') ||
			(c == 'l') ||
			(c == 'j') ||
			(c == 'z'))
		return (1);
	return (0);
}

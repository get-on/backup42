#include "libft.h"

char	*ft_strncpy(char *s1, const char *s2, size_t n)
{
	char *s;

	s = s1;
	while (n)
	{
		if (*s2)
			*s++ = *s2++;
		else
			*s++ = '\0';
		n--;
	}
	return (s1);
}
